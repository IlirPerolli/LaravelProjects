    public function createReply(Request $request){
        $user = Auth::user();
        $data = [
            'comment_id'=>$request->post_id,
            'author'=>$user->name,
            'photo'=>$user->photo->file,
            'email'=>$user->email,
            'body'=>$request->body,

        ];
        CommentReply::create($data);
        session()->flash('reply_message', 'Your reply has been submitted and is waiting moderation');
        return back();
    }
